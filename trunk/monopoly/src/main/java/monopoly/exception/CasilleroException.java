package monopoly.exception;

public class CasilleroException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8878397796894579644L;

	public CasilleroException() {
		super();
	}

	public CasilleroException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CasilleroException(String message, Throwable cause) {
		super(message, cause);
	}

	public CasilleroException(String message) {
		super(message);
	}

	public CasilleroException(Throwable cause) {
		super(cause);
	}

	
	
}
